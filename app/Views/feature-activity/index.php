                        Blantyre during the commemoration of 
                        International Albinism Awareness Day held under the theme: ‘United in making our voices heard.’
                    </p>
                    <p>
                        Hara said the development will also help in early detection of skin infections in all other person’s 
                        with albinism in different communities and receive support accordingly.
                    </p>
                    <p>
                        “The clinics will aim at assessing their eye sight and skin condition so that if something seems not right, 
                        then they should be helped instantly and referred to a bigger hospital where need be,” said Hara.
                    </p>
                    <p>
                        Association for People with Albinism (APAM) president, Young Muhamba asked government to 
                        improve the supply chain of sunscreen 
                        lotion to reduce the risk of skin cancer infection among people with albinism.
                    </p>
                    <p>
                        “The government should also open a sunscreen factory for easy accessibility because skin cancer is claiming a lot of our lives,” Muhamba said.
                    </p>
